public class Arithmetic {
    public static void main(String[] args) {
        int i1 = 12;
        char c1 = 'A';
        System.out.println(c1 + i1);

        long l1 = 343L;
        double d1 = 13.5D;
        System.out.println(l1 + d1);

        float f1 = 14.3F, f2 = 3.2F;
        System.out.println(f1 % f2);
    }
}